/*********************************************************************************************************
* 函数名称：RunClockPer2Ms
* 函数功能：计数模块，每2ms调用一次 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
int Day(int tick,  int day)
{
	int i, j;
	int monthmax = 0;
	int arrMonth[12] = { 31,29,31,30,31,30,31,31,30,31,30,31 };

	for (i = 0, j = 0; i < 366; i++)
	{
		if (tick <= monthmax)
		{
			day = tick - (monthmax - arrMonth[j-1]);
		}
		else if(tick > monthmax)
		{
			monthmax += arrMonth[j];
			day = tick - (monthmax - arrMonth[j]);
			j++;
		}
	}
	return (day);
}
int Month(int tick, int month)
{
	int i,j;
	int monthmax=0;
	int arrMonth[12] = { 31,29,31,30,31,30,31,31,30,31,30,31 };

	for (i = 0,j=0; i < 366; i++)
	{
		if (tick <= monthmax)
		{
			month = j;
		}
		else 
		{
			monthmax += arrMonth[j];
			j++;
		}
	}
	return (month);
}
void week(int tick, char str[20])
{
	int y = 0;

	if (tick > 5)
	{
		y = (tick - 5) % 7;
	}
	if (tick <= 5)
	{
		y = tick + 2;
	}
	switch (y)
	{
	case mon: strcpy(str, "Monday"); break;
	case tues: strcpy(str,  "Tues"); break;
	case wed: strcpy(str, "Wednes"); break;
	case thur: strcpy(str, "Thur"); break;
	case fri: strcpy(str,  "Fri"); break;
	case sat: strcpy(str,  "Sat"); break;
	case sun: strcpy(str, "Sun"); break;
	case sun_1: strcpy(str, "Sun"); break;
	default:
		break;
	}
}

void  RunClockPer2Ms(void)
{
  static i16 s_iCnt500 = 0;

  if(499 <= s_iCnt500 && 0 == s_iClockPauseFlag)
  {
    s_itick++;
    s_iday=Day(s_itick, s_iday);
    s_imonth=Month(s_itick, s_imonth);
    week(s_itick, strweek);
    s_iCnt500 = 0;
  }
  else
  {
    s_iCnt500++;
  }
}

