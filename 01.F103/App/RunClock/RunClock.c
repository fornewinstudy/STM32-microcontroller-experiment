/*********************************************************************************************************
* 函数名称：GetTimeVal
* 函数功能：获取当前的时间值 
* 输入参数：时间值的类型
* 输出参数：void
* 返 回 值：获取的时间值，小时、分钟或秒
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
i16   GetTimeVal(u8 type)
{
  i16 timeVal;
  switch(type)
  {
  case TIME_VAL_HOUR:
    timeVal = s_iday;
    break;
  case TIME_VAL_MIN:
    timeVal = s_imonth;
    break;
  default:
    break;
  } 
  
  return(timeVal);
}
void GetWeekVal(u8 type,char str[20])
{
  char weekVal[20];
  switch(type)
  {
  case TIME_VAL_SEC:
    strcpy(weekVal,  strweek);
    break;
  default:
    break;
  } 
  strcpy(str,weekVal);
}
